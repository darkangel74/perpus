    private void btninsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsertActionPerformed
        Buku buku = new Buku();
        try {
            BukuQuery dao = new BukuQuery(conn.getCon());
            buku.setJudul(jTextField1.getText());
            buku.setPenerbit(jTextField2.getText());
            buku.setPengarang(jTextField3.getText());
            buku.setDesc(jTextArea1.getText());
            dao.insertBuku(buku);
            clearForm();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btninsertActionPerformed

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        Buku buku = new Buku();
        buku.setJudul(jTextField1.getText());
        buku.setPenerbit(jTextField2.getText());
        buku.setPengarang(jTextField3.getText());
        buku.setDesc(jTextArea1.getText());
        try {
            BukuQuery dao = new BukuQuery(conn.getCon());
            dao.updateBuku(jTextField4.getText(), buku);
            clearForm();
        } catch (SQLException e) {
             Logger.getLogger(FormPerpus.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnupdateActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        String id = jTextField4.getText();
        try {
            BukuQuery dao = new BukuQuery(conn.getCon());
            dao.deleteBuku(id);
            clearForm();
        } catch (Exception x) {
        }
    }//GEN-LAST:event_btndeleteActionPerformed

